package com.entity;

public class movies_review {
	private int mov_id;
	private int user_id;
	private String mov_time;
	private String mov_lik;
	public int getMov_id() {
		return mov_id;
	}
	public void setMov_id(int mov_id) {
		this.mov_id = mov_id;
	}
	public int getUser_id() {
		return user_id;
	}
	public void setUser_id(int user_id) {
		this.user_id = user_id;
	}
	public String getMov_time() {
		return mov_time;
	}
	public void setMov_time(String mov_time) {
		this.mov_time = mov_time;
	}
	public String getMov_lik() {
		return mov_lik;
	}
	public void setMov_lik(String mov_lik) {
		this.mov_lik = mov_lik;
	}
}
