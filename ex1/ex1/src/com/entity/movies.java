package com.entity;

public class movies {
	private int mov_id;
	private String mov_name;
	private String mov_intro;
	private String mov_time;
	private String mov_cou;
	private String mov_la;
	private String mov_rel;
	public int getMov_id() {
		return mov_id;
	}
	public void setMov_id(int mov_id) {
		this.mov_id = mov_id;
	}
	public String getMov_name() {
		return mov_name;
	}
	public void setMov_name(String mov_name) {
		this.mov_name = mov_name;
	}
	public String getMov_intro() {
		return mov_intro;
	}
	public void setMov_intro(String mov_intro) {
		this.mov_intro = mov_intro;
	}
	public String getMov_time() {
		return mov_time;
	}
	public void setMov_time(String mov_time) {
		this.mov_time = mov_time;
	}
	public String getMov_cou() {
		return mov_cou;
	}
	public void setMov_cou(String mov_cou) {
		this.mov_cou = mov_cou;
	}
	public String getMov_la() {
		return mov_la;
	}
	public void setMov_la(String mov_la) {
		this.mov_la = mov_la;
	}
	public String getMov_rel() {
		return mov_rel;
	}
	public void setMov_rel(String mov_rel) {
		this.mov_rel = mov_rel;
	}
}
