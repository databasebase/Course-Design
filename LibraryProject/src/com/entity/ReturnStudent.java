package com.entity;

public class ReturnStudent {
	private int return_student_id;
	private int stu_id;
	private int book_id;
	private String return_date;
	private int manager_id;
	private int book_health;
	public int getReturn_student_id() {
		return return_student_id;
	}
	public void setReturn_student_id(int return_student_id) {
		this.return_student_id = return_student_id;
	}
	public int getStu_id() {
		return stu_id;
	}
	public void setStu_id(int stu_id) {
		this.stu_id = stu_id;
	}
	public int getBook_id() {
		return book_id;
	}
	public void setBook_id(int book_id) {
		this.book_id = book_id;
	}
	public String getReturn_date() {
		return return_date;
	}
	public void setReturn_date(String return_date) {
		this.return_date = return_date;
	}
	public int getManager_id() {
		return manager_id;
	}
	public void setManager_id(int manager_id) {
		this.manager_id = manager_id;
	}
	public int getBook_health() {
		return book_health;
	}
	public void setBook_health(int book_health) {
		this.book_health = book_health;
	}
	
}
