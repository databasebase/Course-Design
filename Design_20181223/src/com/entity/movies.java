package com.entity;

public class movies {
	private int movie_id;
	private String movie_name;
	private String movie_intro;
	private String movie_time;
	private String movie_cou;
	private String movie_la;
	private String movie_rel;
	public int getMov_id() {
		return movie_id;
	}
	public void setMov_id(int movie_id) {
		this.movie_id = movie_id;
	}
	public String getMov_name() {
		return movie_name;
	}
	public void setMov_name(String movie_name) {
		this.movie_name = movie_name;
	}
	public String getMov_intro() {
		return movie_intro;
	}
	public void setMov_intro(String movie_intro) {
		this.movie_intro = movie_intro;
	}
	public String getMov_time() {
		return movie_time;
	}
	public void setMov_time(String movie_time) {
		this.movie_time = movie_time;
	}
	public String getMov_cou() {
		return movie_cou;
	}
	public void setMov_cou(String movie_cou) {
		this.movie_cou = movie_cou;
	}
	public String getMov_la() {
		return movie_la;
	}
	public void setMov_la(String movie_la) {
		this.movie_la = movie_la;
	}
	public String getMov_rel() {
		return movie_rel;
	}
	public void setMov_rel(String movie_rel) {
		this.movie_rel = movie_rel;
	}
}
